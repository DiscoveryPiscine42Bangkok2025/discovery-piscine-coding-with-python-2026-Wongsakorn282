#!/usr/bin/env python3

first_number = float(input("Give me the first number: "))
second_number = float(input("Give me the second number: "))

print("Thank you!")
print(f"{int(first_number)} + {int(second_number)} = {int(first_number + second_number)}")
print(f"{int(first_number)} - {int(second_number)} = {int(first_number - second_number)}")
print(f"{int(first_number)} / {int(second_number)} = {int(first_number / second_number)}")
print(f"{int(first_number)} * {int(second_number)} = {int(first_number * second_number)}")
